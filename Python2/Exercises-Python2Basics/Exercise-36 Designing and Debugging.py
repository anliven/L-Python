# If 语句的规则
# 每一个“if 语句”必须包含一个 else.
# 嵌套不要超过 2 层，最好尽量保持只有 1 层。
# 将“if 语句”当做段落来对待，在这种组合的最前面和最后面留一个空行以作区分。
# 布尔测试应该很简单，如果很复杂，需要将它们的运算事先放到一个变量里，并且为变量取一个好名字。

# 循环的规则
# 在循环的对象数量固定或者有限的情况下,使用“for 循环”。
# 只有在循环永不停止时使用“while 循环”。

# 调试(debug)的小技巧
# 使用 print 在各个你想要检查的关键环节将关键变量打印出来，从而检查哪里是否有错。
# 让程序一部分一部分地运行起来。写一点，运行一点，再修改一点。